# What is a Function?
# A function is a block of code designed to perform a specific task. 
# Functions will let you do roughly the same calculation multiple times without duplicating any code.
# Every Function composed of two parts:
    # Header
        # def - keyword which tells python we are defining a function
        # return - keyword which tells python we are exiting a function
    # Body






def avg_of_two_num(num1, num2):
    return (num1 + num2)/2

result = avg_of_two_num(50,60)
print(result)


