# Insert breakpoint where to start debugging (insertion point)       - press F9
# To remove a breakpoint                                             - press F9 again
# To the run the application till the breakpoint                     - Press F5
# To execute 1 line at a time                                        - Press F10
# To step into a funtion                                             - Press F11
# Step out of a function                                             - Press Shift F11
# Stop the debugger                                                  - Press Shift F5
